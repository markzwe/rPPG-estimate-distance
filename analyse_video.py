def save_region_results(results, input_path):
    """Save one CSV per region (pos/vel/acc xyz, nose-subtracted)."""
    base = input_path.rstrip(os.sep)
    header = "frame,pos_x,pos_y,pos_z,vel_x,vel_y,vel_z,acc_x,acc_y,acc_z"
    for region_name, data in results.items():
        out_path = f"{base}_{region_name}.csv"
        n_frames = data["position"].shape[0]
        rows = []
        for i in range(n_frames):
            p = data["position"][i]
            v = data["velocity"][i]
            a = data["acceleration"][i]
            rows.append(
                f"{i},{p[0]:.4f},{p[1]:.4f},{p[2]:.4f},"
                f"{v[0]:.4f},{v[1]:.4f},{v[2]:.4f},"
                f"{a[0]:.4f},{a[1]:.4f},{a[2]:.4f}"
            )
        with open(out_path, "w") as f:
            f.write(header + "\n")
            f.write("\n".join(rows))
        print(f"  Saved: {out_path}")


def save_pose(pose_sequence, input_path):
    """Save global head pose (yaw, pitch, roll) per frame."""
    base = input_path.rstrip(os.sep)
    out_path = f"{base}_pose.csv"
    with open(out_path, "w") as f:
        f.write("frame,yaw,pitch,roll\n")
        for i, (yaw, pitch, roll) in enumerate(pose_sequence):
            f.write(f"{i},{yaw:.4f},{pitch:.4f},{roll:.4f}\n")
    print(f"  Saved: {out_path}")



def save_landmarks(landmark_sequence, input_path, n_points=468):
    """
    Save all 468 nose-subtracted point positions per frame.

    Columns: frame, pt0_x, pt0_y, pt0_z, pt1_x, pt1_y, pt1_z, ..., pt467_z
    This is the file to use when comparing point-by-point with another method.
    Point IDs match bfm_468_pointmap.json exactly.
    """
    base = input_path.rstrip(os.sep)
    out_path = f"{base}_landmarks.csv"

    # Build header: frame, pt0_x, pt0_y, pt0_z, pt1_x, ...
    coord_headers = []
    for i in range(n_points):
        coord_headers += [f"pt{i}_x", f"pt{i}_y", f"pt{i}_z"]
    header = ["frame"] + coord_headers

    with open(out_path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(header)
        for frame_idx, pts in enumerate(landmark_sequence):
            # pts shape: (468, 3)
            row = [frame_idx] + pts.flatten().round(4).tolist()
            writer.writerow(row)

    print(f"  Saved: {out_path}")


def save_landmarks_with_depth(landmark_sequence, pixel_sequence, depth_sequence, input_path, n_points=468):
    """
    Save 468 points per frame with:
      - nose-subtracted 3D coordinates: pt{i}_x, pt{i}_y, pt{i}_z
      - projected image pixels: pt{i}_px, pt{i}_py
      - UniDepth depth value: pt{i}_depth

    Also saves median_depth_m for each frame.
    """
    base = input_path.rstrip(os.sep)
    out_path = f"{base}_landmarks_depth.csv"

    headers = ["frame", "median_depth_m", "valid_depth_points"]
    for i in range(n_points):
        headers += [
            f"pt{i}_x", f"pt{i}_y", f"pt{i}_z",
            f"pt{i}_px", f"pt{i}_py",
            f"pt{i}_depth",
        ]

    with open(out_path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(headers)

        for frame_idx, (pts_3d, pts_px, depths) in enumerate(zip(landmark_sequence, pixel_sequence, depth_sequence)):
            valid_depths = [d for d in depths if d != ""]
            median_depth = float(np.median(valid_depths)) if valid_depths else ""
            valid_count = len(valid_depths)

            row = [
                frame_idx,
                round(median_depth, 4) if median_depth != "" else "",
                valid_count,
            ]

            for point_idx in range(n_points):
                x, y, z = pts_3d[point_idx]
                px, py = pts_px[point_idx]
                d = depths[point_idx]
                row += [
                    round(float(x), 4),
                    round(float(y), 4),
                    round(float(z), 4),
                    round(float(px), 4),
                    round(float(py), 4),
                    round(float(d), 4) if d != "" else "",
                ]

            writer.writerow(row)

    print(f"  Saved: {out_path}")


