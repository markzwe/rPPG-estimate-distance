def parse_args():
    parser = argparse.ArgumentParser(description="Per-region facial motion analyser")
    parser.add_argument("-c", "--config",   type=str, default="configs/mb1_120x120.yml")
    parser.add_argument("-f", "--input",    type=str, required=True,
                        help="Path to directory containing image frames")
    parser.add_argument("--fps",            type=float, default=30.0,
                        help="Frames per second (default: 30.0)")
    parser.add_argument("--onnx",           action="store_true", default=False,
                        help="Use ONNX runtime for the 3DMM regressor (recommended)")
    parser.add_argument("--detector",       type=str, default="facebox",
                        choices=["facebox", "scrfd"],
                        help="facebox (default) or scrfd (better for 4-5m footage)")
    parser.add_argument("--point_index",    type=str, default="bfm_468_indices.json",
                        help="Path to the 468-point index JSON (generated by fps_points.py)")
    parser.add_argument("--with_depth",      action="store_true", default=False,
                        help="Run UniDepth and save depth values for the 468 FPS points")
    parser.add_argument("--patch_size",      type=int, default=2,
                        help="Patch radius for median depth sampling. 2 means 5x5 patch")
    parser.add_argument("--focal_length_mm", type=float, default=26.0,
                        help="Camera focal length in mm")
    parser.add_argument("--sensor_width_mm", type=float, default=36.0,
                        help="Sensor width assumption in mm")
    parser.add_argument("--sensor_width_px", type=int, default=4032,
                        help="Sensor/reference width in pixels for focal length conversion")
    parser.add_argument("--source_width_px", type=int, default=1280,
                        help="Original video/source width before processing")
    return parser.parse_args()


