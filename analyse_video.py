# Main
# ---------------------------------------------------------------------------

def main():
    args = parse_args()

    # --- Load 3DMM config and regressor ---
    cfg = yaml.load(open(args.config), Loader=yaml.SafeLoader)
    tddfa = TDDFA_ONNX(**cfg) if args.onnx else TDDFA(**cfg)

    # --- Optional UniDepth model for 468-point depth sampling ---
    depth_model = None
    device = None
    if args.with_depth:
        print("Loading UniDepthV2 for 468-point depth sampling...")
        device = get_device()
        depth_model = UniDepthV2.from_pretrained("lpiccinelli/unidepth-v2-vitl14").to(device)
        depth_model.eval()
        print(f"  UniDepth device: {device}")

    # --- Load detector ---
    if args.detector == "scrfd":
        print("Detector: SCRFD (InsightFace)")
        scrfd_app = load_scrfd()
        def detect(frame): return scrfd_detect(scrfd_app, frame)
    else:
        print("Detector: FaceBoxes")
        fb = FaceBoxes()
        def detect(frame): return fb(frame)

    # --- Load 468-point index ---
    # Raises a clear error with instructions if the file doesn't exist yet.
    print(f"Loading point index: {args.point_index}")
    point_indices, region_map, nose_dense_idx = load_point_index(args.point_index)
    n_points = len(point_indices)
    print(f"  {n_points} points loaded")

    # --- Load PNG images ---
    if not os.path.isdir(args.input):
        raise RuntimeError(f"Directory not found: {args.input}")

    image_exts = (".png", ".jpg", ".jpeg", ".bmp", ".webp")
    image_files = sorted(
        f for f in os.listdir(args.input) if f.lower().endswith(image_exts)
    )
    if not image_files:
        raise RuntimeError(f"No image files found in: {args.input}")

    print(f"Input:  {args.input}")
    print(f"FPS:    {args.fps:.2f}  |  Frames: {len(image_files)}")

    # --- Process frames ---
    # ver_sequence_68:   (3, 68) per frame — for region kinematics
    # ver_sequence_dense: (468, 3) per frame — FPS-selected dense points
    ver_sequence_68       = []
    ver_sequence_dense    = []
    pixel_sequence_dense  = []
    depth_sequence_dense  = []
    pose_sequence         = []
    param_sequence        = []
    skipped = 0

    for frame_idx, image_file in enumerate(image_files):
        frame = cv2.imread(os.path.join(args.input, image_file))
        if frame is None:
            skipped += 1
            continue

        detected = detect(frame)
        if not detected:
            skipped += 1
            continue

        detected_box = detected[0]
        coords = detected_box[:4]
        x1, y1, x2, y2 = int(coords[0]), int(coords[1]), int(coords[2]), int(coords[3])

        if (x2 - x1) <= 0 or (y2 - y1) <= 0:
            skipped += 1
            continue

        box = np.array([x1, y1, x2, y2], dtype=np.float32)

        try:
            param_lst, roi_box_lst = tddfa(frame, [box], crop_policy="box")

            # --- 68-point sparse landmarks (for region kinematics) ---
            ver_68 = tddfa.recon_vers(param_lst, roi_box_lst, dense_flag=False)[0]
            ver_sequence_68.append(ver_68)

            # --- Dense mesh → select 468 FPS points ---
            # dense_flag=True gives shape (3, 38365)
            ver_full = tddfa.recon_vers(param_lst, roi_box_lst, dense_flag=True)[0]

            # Subtract nose position (dense mesh nose index) for relative motion
            nose_pos = ver_full[:, nose_dense_idx]             # (3,)
            ver_relative = ver_full - nose_pos[:, np.newaxis]  # (3, 38365)

            # Extract the 468 FPS-selected points, shape → (468, 3)
            pts_468_relative = ver_relative[:, point_indices].T
            ver_sequence_dense.append(pts_468_relative)

            # Projected 2D image pixels (non-nose-subtracted mesh x/y)
            pts_468_projected = ver_full[:2, point_indices].T  # (468, 2)
            pixel_sequence_dense.append(pts_468_projected)

            # Optional: sample UniDepth at each projected 2D landmark pixel
            if args.with_depth:
                depth_map = predict_depth_map(depth_model, frame, args, device)
                point_depths = []
                for px, py in pts_468_projected:
                    d = sample_depth(depth_map, px, py, patch_size=args.patch_size)
                    point_depths.append(d)
                depth_sequence_dense.append(point_depths)

            param_sequence.append(param_lst[0])

            if frame_idx % 50 == 0 or frame_idx + 1 == len(image_files):
                print(f"  Frame {frame_idx + 1}/{len(image_files)}...")

        except Exception as e:
            print(f"  Frame {frame_idx} ({image_file}) failed: {e}")
            skipped += 1

    print(f"Processed {len(ver_sequence_68)} frames, skipped {skipped}")

    if len(ver_sequence_68) < 3:
        print("Not enough frames for kinematics (need at least 3). Exiting.")
        return

    # --- Head pose ---
    print("Extracting head pose...")
    for param in param_sequence:
        _, pose = calc_pose(param)
        pose_sequence.append(pose)

    # --- Region kinematics (from 68-pt sparse landmarks, same as before) ---
    print("Computing region kinematics...")
    results = process_video_landmarks(ver_sequence_68, args.fps)

    # --- Save ---
    print("Saving CSVs...")
    save_region_results(results, args.input)
    save_pose(pose_sequence, args.input)
    save_landmarks(ver_sequence_dense, args.input, n_points=n_points)

    if args.with_depth:
        save_landmarks_with_depth(
            ver_sequence_dense,
            pixel_sequence_dense,
            depth_sequence_dense,
            args.input,
            n_points=n_points,
        )

    print("Done.")


if __name__ == "__main__":
    main()
