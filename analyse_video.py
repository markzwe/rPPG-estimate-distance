def get_device():
    """Use Apple Silicon MPS if available, otherwise CPU."""
    return torch.device("mps" if torch.backends.mps.is_available() else "cpu")


def build_intrinsics(frame_width, frame_height, args):
    """
    Build camera intrinsics using the same scaling logic as the working
    InsightFace + UniDepth pipeline.
    """
    focal_length_px_sensor = (args.focal_length_mm * args.sensor_width_px) / args.sensor_width_mm
    scale = frame_width / args.source_width_px
    f = focal_length_px_sensor * scale
    cx = frame_width / 2
    cy = frame_height / 2
    return [
        [f, 0, cx],
        [0, f, cy],
        [0, 0, 1],
    ]


def predict_depth_map(depth_model, frame_bgr, args, device):
    """Run UniDepthV2 and return a metric depth map with shape (H, W)."""
    frame_rgb = cv2.cvtColor(frame_bgr, cv2.COLOR_BGR2RGB)
    h, w = frame_bgr.shape[:2]

    intrinsics = build_intrinsics(w, h, args)
    intrinsics_tensor = torch.tensor([intrinsics], dtype=torch.float32).to(device)
    camera = Pinhole(K=intrinsics_tensor).to(device)

    image_tensor = (
        torch.from_numpy(frame_rgb)
        .permute(2, 0, 1)
        .unsqueeze(0)
        .float()
        .to(device) / 255.0
    )

    with torch.no_grad():
        predictions = depth_model.infer(image_tensor, camera=camera)

    return predictions["depth"][0, 0].cpu().numpy()


def sample_depth(depth_map, px, py, patch_size=2):
    """
    Sample a median depth value from a local patch around projected pixel (px, py).
    Returns an empty string if the point is outside the image.
    """
    h, w = depth_map.shape[:2]
    x = int(round(float(px)))
    y = int(round(float(py)))

    if not (0 <= x < w and 0 <= y < h):
        return ""

    x1 = max(x - patch_size, 0)
    x2 = min(x + patch_size + 1, w)
    y1 = max(y - patch_size, 0)
    y2 = min(y + patch_size + 1, h)

    patch = depth_map[y1:y2, x1:x2]
    if patch.size == 0:
        return ""

    return float(np.median(patch))


